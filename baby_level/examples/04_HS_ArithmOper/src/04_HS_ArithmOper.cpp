#include <iostream>
using namespace std;

int main() {
	int year = 365;
	int mb = 1024;
	double goldmass = 19.32;
	cout << "Number of seconds in 2019: " << year*24*60*60 <<endl;
	cout << "In 1 mb: " << mb*mb << "b" <<endl;
	cout << "1 liter of gold: " << 1*goldmass << "kg" <<endl;
	return 0;
}
